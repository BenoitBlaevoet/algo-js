let day = prompt("Enter the current day of the week (1 to 7)");

switch(day){
    case "1":
        alert("We are Monday");
        break;
    case "2":
        alert("We are Tuesday");
        break;
    case "3":
        alert("We are Wednesday");
        break;
    case "4":
        alert("We are Thursday");
        break;
    case "5":
        alert("We are Friday");
        break;
    case "6":
        alert("We are Saturday");
        break;
    default :
        alert("We are Sunday");
        break;
}
